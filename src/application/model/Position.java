package application.model;

public class Position {
	
	// Fields
	private double x;
	private double y;
	// end Fields
	
	// Constructor
	public Position(double x, double y) {
		this.x = x;
		this.y = y;
	};
	// end Constructor
	
	// Getters
	public double getX() {
		return x;
	}
	public double getY() {
		return y;
	}
	// end Getters
	
}
